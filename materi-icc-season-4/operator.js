const angka1 = 25;
const angka2 = 7;

// Penjumlahan
const hasilTambah = angka1 + angka2;
console.log(`Penjumlahan: ${hasilTambah}`); // 32

// Pengurangan
const hasilKurang = angka1 - angka2;
console.log(`Pengurangan: ${hasilKurang}`); // 18

// Perkalian
const hasilKali = angka1 * angka2;
console.log(`Perkalian: ${hasilKali}`); // 175

// Pembagian
const hasilBagi = angka1 / angka2;
console.log(`Pembagian: ${hasilBagi}`); // sekitar 3.57

// Modulus (sisa bagi)
const hasilModulus = angka1 % angka2;
console.log(`Modulus: ${hasilModulus}`); // 4